# Incluir novos registro na lista
numerica = [1,2,3,4,5]
animal = ['cachorro', 'gato', 'cavalo', 'elefante', 'arara', 'baleia']
animal.append('lobo')
print(animal)

# Remover um registro na lista
animal.pop(3)
print(animal)

# Remover registro na lista pelo o nome
animal.remove('cavalo')
print(animal)

print("\n Ordenar uma lista")
animal.sort()
print(animal)

# Ordenar de forma reversa
numerica.reverse()
print(numerica)

